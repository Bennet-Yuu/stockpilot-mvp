const reverseTranslations = Object.fromEntries(Object.entries(translations).map(([english, chinese]) => [chinese, english]));

export function readLocalePreference(storage?: Storage): Locale {
  try {
    return storage?.getItem(LOCALE_KEY) === "en" ? "en" : "zh";
  } catch {
    return "zh";
  }
}

export function writeLocalePreference(locale: Locale, storage?: Storage): void {
  try {
    storage?.setItem(LOCALE_KEY, locale);
  } catch {
    // A blocked browser storage should not block the research workflow.
  }
}

function translateValue(value: string, locale: Locale): string {
  const trimmed = value.trim();
  const translated = locale === "zh" ? translations[trimmed] : reverseTranslations[trimmed];
  if (translated) {
    const start = value.indexOf(trimmed);
    return `${value.slice(0, start)}${translated}${value.slice(start + trimmed.length)}`;
  }
  // Some labels contain a live number (for example, a P/L amount). Replace
  // known phrases inside the text so those dynamic values still localize.
  const pairs = locale === "zh" ? Object.entries(translations) : Object.entries(reverseTranslations);
  return pairs.reduce((current, [from, to]) => from.length > 2 ? current.split(from).join(to) : current, value);
}

export function translateDom(root: Element, locale: Locale): void {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const nodes: Text[] = [];
  let current: Node | null = walker.nextNode();
  while (current) {
    if (current.nodeValue?.trim()) nodes.push(current as Text);
    current = walker.nextNode();
  }
  nodes.forEach((node) => {
    const next = translateValue(node.nodeValue ?? "", locale);
    if (next !== node.nodeValue) node.nodeValue = next;
  });
  root.querySelectorAll<HTMLInputElement | HTMLTextAreaElement | HTMLSelectElement>("input, textarea, select, [aria-label], [title]").forEach((element) => {
    ["placeholder", "aria-label", "title"].forEach((attribute) => {
      const value = element.getAttribute(attribute);
      if (value) element.setAttribute(attribute, translateValue(value, locale));
    });
  });
}
